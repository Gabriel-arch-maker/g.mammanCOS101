fn main() {
    // Addition
    let sum = 5550 + 7310;
    println!("The sum of 5550 and 7310 = {}", sum);

    // Subtraction
    let diff:f32 = 95.5 - 4.3;
    println!("The difference of 95.3 and 4.3 = {}", diff);

    // Multiplication
    let product:i32 = 4 * 30;
    println!("The multiple of 4 abd 30 = {}", product);

    // Division
    let div = 56.7 / 32.2;
    println!("The division of 56.7 and 32.2 = {}", div);

    // Remainder
    let rmnd = 43 % 5;
    println!("The remainder of 43 and 5 = {}", rmnd);
}
